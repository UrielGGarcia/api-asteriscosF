function nombreAsteriscos(texto = "usuario default") {
    const letrasN = texto.toUpperCase().split("");
    const alto = letras.A.length;
    let resultado = "";

    for (let fila = 0; fila < alto; fila++) {
        let linea = "";
        letrasN.forEach(letra => {
            if (letras[letra]) {
                linea += letras[letra][fila] + " ";
            } else {
                linea += "     ";
            }
        });
        resultado += linea + "\n";
    }

    return resultado;
}


